class Project < ApplicationRecord
	has_many :time_entries
	validates :name, presence: true, uniqueness: true, length: { maximum: 30 }, format: { with: /\w/}

#========================================================================
#========================= Methods ======================================
#========================================================================

	def self.clean_old
		old_projects = Project.where('updated_at < ?', (Time.now - 1.week))
		old_projects.destroy_all
	end

#========================================================================
#====================== Relationships ===================================
#========================================================================
	

#========================================================================
#====================== Validations =====================================
#========================================================================


end
